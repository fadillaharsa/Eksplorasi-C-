#include <iostream>
using namespace std;

int main(){
    bool jawaban; 	// Mendeklarasikan variabel jawaban bertipe bool
    jawaban = 1;
    cout << "Jawaban = " << jawaban << endl;
    jawaban = 0;
    cout << "Jawaban = " << jawaban << endl;
    jawaban = true;
    cout << "Jawaban = " << jawaban << endl;
    jawaban = false;
    cout << "Jawaban = " << jawaban;
    return 0;
}

#include <iostream>
using namespace std;

int main(){
	//Tipe data short int digunakan untuk bilangan bulat yang terletak antara -32768 s.d. +32667
    short angka;	// Mendeklarasikan variabel angka bertipe short int
    angka = 1;	// Menugaskan 1 ke variabel angka
    cout << "Isi angka = " << angka << endl; //Mencetak variabel angka
    angka = -100;	// Menugaskan -100 ke variabel angka
    cout << "Isi angka = " << angka << endl; //Mencetak variabel angka
    angka = 32667;	// Menugaskan 32667 ke variabel angka
    cout << "Isi angka = " << angka << endl; //Mencetak variabel angka
    return 0;
}

#include <iostream>
using namespace std;

int main(){
    char karakter;	// Mendeklarasikan variabel karakter bertipe char
	karakter = 'A';   // Menugaskan 'A' ke dalam variabel karakter
    cout << "Isi karakter = " << karakter << endl;
    karakter = '2'; // Menugaskan '2' ke dalam variabel karakter
	cout << "Isi karakter = " << karakter << endl;
    karakter = 36; // Menugaskan karakter yang nilai ASCIInya 21 ke dalam variabel karakter
	cout << "Isi karakter = " << karakter;
    return 0;
}

#include <iostream>
using namespace std;

int main(){
    int a, c;	// Mendeklarasikan variabel a dan c bertipe signed int
    unsigned int b, d;	// Mendeklarasikan variabel b dan d bertipe signed int
    a = -1;
    cout << "a = " << a << endl;
    b = -1;
    cout << "b = " << b << endl;
    c = -2;
    cout << "c = " << c << endl;
    d = -2;
    cout << "d = " << d;
    return 0;
}
